void hbtc_glb_vars_init()
{
    UINT8 i;
    for (i=0;i<HBTC_TOTAL_TIMER;i++)
    {
      gl_hbtc_timer[i].timer=0x00;
      gl_hbtc_timer[i].func=FK_NULL_FUNC;
    }
    HBTC_SET_SLEEP_API();
}
